package usermanagement


class Staff {

    private String name
    private  Integer age
    private String profession

    Staff() {
    }

    String getName() {
        return name
    }

    void setName(String name) {
        this.name = name
    }

    Integer getAge() {
        return age
    }

    void setAge(Integer age) {
        this.age = age
    }

    String getProfession() {
        return profession
    }

    void setProfession(String profession) {
        this.profession = profession
    }
}
